	from math import pi

	#Area del quadrato
	def squarea(side):
	  return side**2

	#Area del rettangolo
	def rectarea(height,width):
	  return height*width

	#Area del cerchio
	def circarea(radius):
	  return pi*radius **2

	#Area del triangolo rettangolo
	def rectriarea(cat1,cat2):
	  return cat1*cat2/2
